public class Ausdruecke2 {
  public static void main (String[] args) {
    System.out.println( "14.0%%5 = "+(14.0%5)+" (double)");
    System.out.println( "15 %% 2 = "+(15 % 2) );
    System.out.println( "1 > 2 = "+(1>2) );
    System.out.println( "(1+1)>1 = "+((1+1)>1 ) );
  }
}
