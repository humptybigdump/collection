fish <- data.frame(color, sex, size, weight, age)

# Dummies erstellen
fish$sex_D <- recode(fish$sex, "'male'=0; 'female'=1")
fish$color_D <- recode(fish$color, "'green'=0; 'blue'=1")

# Multiple Regression
multReg <- lm(weight ~ size + age + sex_D, data = fish)
multReg
summary(multReg)
avPlots(multReg)

# standardisierte Koeffizienten
